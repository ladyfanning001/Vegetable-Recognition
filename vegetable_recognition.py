"""# Train & Test split"""

base_dir = Path('/content/Vegetable Images')
train_dir = base_dir / 'train'
test_dir = base_dir / 'test'
validation_dir = base_dir / 'validation'

print("Checking dataset structure...")
print(f"Train directory exists: {train_dir.exists()}")
print(f"Test directory exists: {test_dir.exists()}")
print(f"Validation directory exists: {validation_dir.exists()}")

classes = sorted([d.name for d in train_dir.iterdir() if d.is_dir()])
num_classes = len(classes)

print(f"\nNumber of classes: {num_classes}")
print(f"Classes: {classes}")

def count_images(directory):
    count = 0
    for class_dir in directory.iterdir():
        if class_dir.is_dir():
            count += len(list(class_dir.glob('*.*')))
    return count

train_count = count_images(train_dir)
test_count = count_images(test_dir)
val_count = count_images(validation_dir)

print(f"\nDataset Statistics:")
print(f"Training images: {train_count}")
print(f"Validation images: {val_count}")
print(f"Testing images: {test_count}")
print(f"Total images: {train_count + val_count + test_count}")

"""## 1.3 Data Preprocessing and Augmentation"""

IMG_HEIGHT = 224
IMG_WIDTH = 224
BATCH_SIZE = 32

print(f"Image size: {IMG_HEIGHT} x {IMG_WIDTH}")
print(f"Batch size: {BATCH_SIZE}")

train_datagen = ImageDataGenerator(
    rescale=1./255,
    rotation_range=25,
    width_shift_range=0.2,
    height_shift_range=0.2,
    zoom_range=0.2,
    horizontal_flip=True,
    vertical_flip=False,
    fill_mode='nearest',
    brightness_range=[0.8, 1.2],
    shear_range=0.15
)

val_test_datagen = ImageDataGenerator(rescale=1./255)

train_generator = train_datagen.flow_from_directory(
    str(train_dir),
    target_size=(IMG_HEIGHT, IMG_WIDTH),
    batch_size=BATCH_SIZE,
    class_mode='categorical',
    shuffle=True,
    seed=42
)

val_generator = val_test_datagen.flow_from_directory(
    str(validation_dir),
    target_size=(IMG_HEIGHT, IMG_WIDTH),
    batch_size=BATCH_SIZE,
    class_mode='categorical',
    shuffle=False,
    seed=42
)

test_generator = val_test_datagen.flow_from_directory(
    str(test_dir),
    target_size=(IMG_HEIGHT, IMG_WIDTH),
    batch_size=BATCH_SIZE,
    class_mode='categorical',
    shuffle=False,
    seed=42
)

class_labels = train_generator.class_indices
print(f"\nClass indices: {class_labels}")

