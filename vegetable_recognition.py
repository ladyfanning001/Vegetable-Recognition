print(f"Total images in training: {train_generator.samples}")
print(f"Total images in validation: {val_generator.samples}")

history = model.fit(
    train_generator,
    steps_per_epoch=steps_per_epoch,
    validation_data=val_generator,
    validation_steps=validation_steps,
    epochs=100,
    callbacks=callback_list,
    verbose=1
)

print("\nTraining completed!")

