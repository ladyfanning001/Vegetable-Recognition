"""# 2. Model Building

## 2.1 Build CNN Architecture with Conv2D and MaxPooling
"""

model = models.Sequential([
    layers.Conv2D(32, (3, 3), activation='relu', padding='same',
                  input_shape=(IMG_HEIGHT, IMG_WIDTH, 3), name='conv_block1_1'),
    layers.Conv2D(32, (3, 3), activation='relu', padding='same', name='conv_block1_2'),
    layers.MaxPooling2D((2, 2), name='pool_block1'),
    layers.Dropout(0.25),

    layers.Conv2D(64, (3, 3), activation='relu', padding='same', name='conv_block2_1'),
    layers.Conv2D(64, (3, 3), activation='relu', padding='same', name='conv_block2_2'),
    layers.MaxPooling2D((2, 2), name='pool_block2'),
    layers.Dropout(0.25),

    layers.Conv2D(128, (3, 3), activation='relu', padding='same', name='conv_block3_1'),
    layers.Conv2D(128, (3, 3), activation='relu', padding='same', name='conv_block3_2'),
    layers.MaxPooling2D((2, 2), name='pool_block3'),
    layers.Dropout(0.25),

    layers.Conv2D(256, (3, 3), activation='relu', padding='same', name='conv_block4_1'),
    layers.Conv2D(256, (3, 3), activation='relu', padding='same', name='conv_block4_2'),
    layers.MaxPooling2D((2, 2), name='pool_block4'),
    layers.Dropout(0.25),

    layers.Flatten(name='flatten'),
    layers.Dense(512, activation='relu', name='dense1'),
    layers.Dropout(0.5),
    layers.Dense(256, activation='relu', name='dense2'),
    layers.Dropout(0.5),
    layers.Dense(num_classes, activation='softmax', name='output')
])

print("Model Architecture:")
model.summary()

optimizer = Adam(learning_rate=0.0001)
model.compile(
    optimizer=optimizer,
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

print("\nModel compiled successfully!")

"""## 2.2 Define Training Callbacks"""

callback_list = [
    ModelCheckpoint(
        filepath='best_model.h5',
        monitor='val_accuracy',
        save_best_only=True,
        mode='max',
        verbose=1
    ),

    EarlyStopping(
        monitor='val_accuracy',
        patience=20,
        restore_best_weights=True,
        verbose=1,
        min_delta=0.001
    ),

    ReduceLROnPlateau(
        monitor='val_accuracy',
        factor=0.5,
        patience=10,
        min_lr=1e-7,
        verbose=1
    )
]

print("Callbacks configured:")
print("✓ ModelCheckpoint - saves best model based on validation accuracy")
print("✓ EarlyStopping - stops training if val_accuracy doesn't improve for 20 epochs")
print("✓ ReduceLROnPlateau - reduces learning rate when accuracy plateaus")

"""## 2.3 Train the Model"""

